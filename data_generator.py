# Factor de desarrollo económico por estado
FACTOR_DEV = {
    "CDMX": 2.0, "Nuevo León": 1.9, "Jalisco": 1.7,
    "Guanajuato": 1.6, "Baja California": 1.6, "Querétaro": 1.6,
    "Coahuila": 1.5, "Sonora": 1.4, "Tamaulipas": 1.4, "Chihuahua": 1.4,
    "Estado de México": 1.3, "Aguascalientes": 1.3, "Colima": 1.1,
    "Morelos": 1.0, "Sinaloa": 1.0, "Durango": 0.9, "Nayarit": 0.9,
    "Yucatán": 0.9, "San Luis Potosí": 0.9, "Michoacán": 0.85,
    "Tabasco": 0.85, "Baja California Sur": 0.85, "Tlaxcala": 0.8,
    "Zacatecas": 0.75, "Campeche": 0.75, "Quintana Roo": 0.8,
    "Hidalgo": 0.75, "Puebla": 0.8, "Veracruz": 0.75,
    "Oaxaca": 0.45, "Guerrero": 0.40, "Chiapas": 0.35,
}

def generate_data():
    rows = []
    for i, (municipio, estado, lat, lon, frontera) in enumerate(MUNICIPIOS):
        factor_dev = FACTOR_DEV.get(estado, 1.0)

        densidad_industrial = np.random.beta(2, 5) * 65 * factor_dev

        rezago_educativo = max(0, min(100,
            74 - densidad_industrial * 0.55 + np.random.normal(0, 9)
        ))
        if estado in ["Oaxaca", "Chiapas", "Guerrero"]:
            rezago_educativo = min(100, rezago_educativo + np.random.uniform(12, 28))
        elif estado in ["Veracruz", "Hidalgo", "Puebla", "Michoacán", "Tabasco"]:
            rezago_educativo = min(100, rezago_educativo + np.random.uniform(5, 15))

        pobreza = max(0, min(100,
            rezago_educativo * 0.55 + np.random.normal(10, 7)
        ))

        densidad_escolar = max(1, np.random.normal(8, 2.5) - pobreza * 0.03)

        salario_base = 278.80 if frontera else 212.55
        salario = salario_base * np.random.uniform(0.85, 1.35)

        decil = max(1, min(10, int(10 - pobreza / 11 + np.random.normal(0, 0.8))))

        if decil >= 9:   amai = "A/B"
        elif decil >= 7: amai = "C+"
        elif decil >= 5: amai = "C"
        elif decil >= 3: amai = "D+"
        elif decil >= 2: amai = "D"
        else:            amai = "E"

        poblacion = int(np.random.lognormal(10.3, 1.3))

        rows.append({
            "mun_id":              i + 1,
            "municipio":           municipio,
            "estado":              estado,
            "lat":                 lat,
            "lon":                 lon,
            "densidad_industrial": round(densidad_industrial, 2),
            "rezago_educativo":    round(rezago_educativo, 2),
            "pobreza_multidim":    round(pobreza, 2),
            "densidad_escolar":    round(densidad_escolar, 2),
            "salario_minimo":      round(salario, 2),
            "decil_ingreso":       decil,
            "amai_2024":           amai,
            "poblacion":           poblacion,
            "zona_frontera":       frontera,
        })

    df = pd.DataFrame(rows)
    df.to_csv("municipios.csv", index=False)
    print(f"✅ {len(df)} municipios | {df['estado'].nunique()} estados")
    return df

if __name__ == "__main__":
    df = generate_data()
    print(df.groupby("estado")["municipio"].count().sort_index().to_string())
